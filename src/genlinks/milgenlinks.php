        Description of each type of file:<br>
        Pension Application Files, based on Federal (not State) service before 
        World War I, usually include official statement of veteran's military 
        service, as well as information of personal nature.</small><br>
        <small>Pensions based on military service for Confederate States of America 
        were authorized by some Southern States but not by Federal Government 
        until 1959. Inquiries about State Pensions should be addressed to the 
        State Archives or equivalent agency at the capital of the veteran's State 
        of residence after the war.<br>
        <br>
        Bounty-Land Warrant Application Files are based on Federal (not State) 
        service before 1856. Documents in bounty-land warrant application file 
        are similar to those in pension application file. In addition, these files 
        usually give veteran's age and place of residence at time application 
        was made.<br>
